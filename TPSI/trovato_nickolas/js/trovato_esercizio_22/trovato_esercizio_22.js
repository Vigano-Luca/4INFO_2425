const somma = (n1, n2) => n1 + n2;
const sottrai = (n1, n2) => n1 - n2;
const moltiplica = (n1, n2) => n1 * n2;
const dividi = (n1, n2) => n1 / n2;

console.log("Risultato somma di 4 e 2:", somma(4, 2));
console.log("Risultato somma di 7 e 5:", somma(7, 5));

console.log("Risultato sottrazione di 4 e 2:", sottrai(4, 2));
console.log("Risultato sottrazione di 7 e 5:", sottrai(7, 5));

console.log("Risultato moltiplicazione di 4 e 2:", moltiplica(4, 2));
console.log("Risultato moltiplicazione di 7 e 5:", moltiplica(7, 5));

console.log("Risultato divisione di 4 e 2:", dividi(4, 2));
console.log("Risultato divisione di 7 e 5:", dividi(7, 5));
